package flightApp.model;

public class BoardingPass {
	
	Ticket ticket;
	Baggage baggage;
	
	
	public Ticket getTicket() {
		return ticket;
	}
	public void setTicket(Ticket ticket) {
		this.ticket = ticket;
	}
	public Baggage getBaggage() {
		return baggage;
	}
	public void setBaggage(Baggage baggage) {
		this.baggage = baggage;
	}
	

}
